if (isLoggedIn()) {
    header('Location: /admin/');
    exit;
}

$error = '';
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = trim($_POST['username'] ?? '');
    $password = $_POST['password'] ?? '';

    if (loginAdmin($pdo, $username, $password)) {
        header('Location: /admin/');
        exit;
    } else {
        $error = 'Invalid username or password.';
    }
}
